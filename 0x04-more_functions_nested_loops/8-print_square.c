#include "main.h"

/**
 * print_square - function to print square
 * @size: arguement of the function
 *
 * Return: 0 number
 */

void print_square(int size)
{
	int i, size;

	for (size > 0; size <= size; size++)
	{
		for (i > 0; i <= size; i++)
		{
			_putchar('#');
			if (size <= 0)
			{
				_putchar('\n');
			}
		}
		_putchar('#');
	}
	_putchar('\n');
}
